/* 
    Operadores Aritmeticos

    + -> Adição
    - -> Subtração
    * -> Multiplicação
    / -> Divisão
    % -> Resto (Não é possivel com números decimais)
    ++ -> Incremento (Adiciona +1)
    -- -> Decremento (Subtrai -1)
    ** -> Potenciação (Math.pow)
*/

    let number = 10

    ++number

    console.log(number)

    --number

    console.log(number)

    console.log(number ** 3)
