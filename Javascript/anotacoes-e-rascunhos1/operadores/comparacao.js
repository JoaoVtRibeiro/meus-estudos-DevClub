/* 
    Operadores de comparação

    ==      -> Igual : Compara o valor, mas não compara o tipo do valor 
    ===     -> Totalmente igual: Compara o valor e o tipo de dado (Recomendado)

    !=      -> Diferente : Compara o valor, mas não compara o tipo do valor 
    !==     -> Totalmente diferente: Compara o valor e o tipo de dado (Recomendado)
*/

const number = "20"

const number2 = 20

console.log(number == number2)

console.log(number === number2)

