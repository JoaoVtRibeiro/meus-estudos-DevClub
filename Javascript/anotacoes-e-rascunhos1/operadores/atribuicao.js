/* 
    Operadores de atribuição

    =   -> Atribuição de um valor
    +=  -> Adição
    -+  -> Subtração
    /=  -> Divisão
    *=  -> Multiplicação
    %=  -> Resto

    Na prática, são quase como atalhos, já fazem o calculo da atribuição com a própria variavel

    let variavel = variavel + 10 é igual a let variavel += 10

*/

    let number = 10

    number += 10

    console.log(number)

    number -= 5

    console.log(number)

    number *= 10

    console.log(number)

    number /= 2

    console.log(number)

    number %= 5
    
    console.log(number)

