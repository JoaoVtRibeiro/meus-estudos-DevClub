/* 
    Estrutura de repetição - While (Enquanto)

    While repete um determinado trecho de código ENQUANTO a condição determinada for verdadeira.

    Obs: Recomendado utilizar o while quando é imprevisel dizer quando o evento irá terminar/acontecer, como dividir um número muito grande por exemplo

*/

let i = 0

while(i <= 20){
    console.log(i)
    i+= 2
}

