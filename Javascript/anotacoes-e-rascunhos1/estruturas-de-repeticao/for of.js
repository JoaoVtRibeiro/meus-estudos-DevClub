/* 
    Estrutura de repetição - For of

    For of repete um trecho de código a partir do número de caracteres de uma string ou de posições em uma array
*/


// Exemplo com string
let string = "texto"

for(value of string){
    console.log(value)
}


// Exemplo com array
let array = ["pessoa1", "pessoa2", "pessoa3"]

for(value of array){
    console.log(value)
}