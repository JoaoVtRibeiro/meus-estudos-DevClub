/* 
    Condicionais - If Else

    Executam um determinado trecho de código SE (if) uma condição determinada for verdadeira e outra SE NÂO (else) for verdadeira.

    if = se
    else = se não

    Obs: Não é obrigatório que um if acompanhe um else (pode ser utilizado sozinho), mas o else só pode ser utilizado acompanhado de um if
*/

let age = 18

if(age >= 18){
    console.log("A pessoa é maior de idade")
} else {
    console.log("A pessoa é menor de idade")
}